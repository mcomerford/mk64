function calculateTextPosition(i)
    return TOP_TEXT_Y + TEXT_HEIGHT * i
end

function randomU16(input)
    if input == 0x560A then
        input = 0
    end
    local s0 = ((input & 0xFF) << 8) ~ input
    input = (bit.byteswap_32(s0) >> 16) & 0xFFFF
    s0 = ((s0 & 0xFF) << 1) ~ input
    local s1 = (s0 >> 1) ~ 0xFF80
    if (s0 & 0x1) == 0 then
        if s1 == 0xAA55 then
            input = 0
        else
            input = s1 ~ 0x1FF4
        end
    else
        input = s1 ~ 0x8180
    end
    return input
end

function randomInt(input)
    return (randomU16(input) / 65535.0 * 100.0) // 1
end

function printOutput()
    local fontColor = emu.islagged() and "LightGray" or "White"
    gui.cleartext()
    gui.text(0, calculateTextPosition(0), "rng prior to item select:       " .. string.format("%d", previousRng), fontColor)
    gui.text(0, calculateTextPosition(1), "a/b/r button presses:           " .. string.format("%d", buttonCounter), fontColor)
    gui.text(0, calculateTextPosition(2), "frame count:                    " .. string.format("%d", frameCounter), fontColor)
    gui.text(0, calculateTextPosition(3), "player 1 position:              " .. string.format("%d", player1Position), fontColor)
    gui.text(0, calculateTextPosition(4), "last selected item:             " .. string.format("%d %s", lastSelectedItem, ITEMS[ITEM_PROBABILITIES[player1Position][lastSelectedItem + 1]]), fontColor)
    gui.text(0, calculateTextPosition(5), "calculated rng for item select: " .. string.format("%d", calculatedRng), fontColor)
    gui.text(0, calculateTextPosition(6), "range limited rng (0-99):       " .. string.format("%d", limitedRng), fontColor)
    gui.text(0, calculateTextPosition(7), "item rng:                       " .. string.format("%d", itemRng), fontColor)
    gui.text(0, calculateTextPosition(8), "item value:                     " .. string.format("%d", itemValue), fontColor)
    gui.text(0, calculateTextPosition(9), "item selected on this frame:    " .. item, fontColor)
end

event.on_bus_exec(function(addr, val, flags)
    previousRng = mainmemory.read_u16_be(RNG_ADDR)
    buttonCounter = mainmemory.read_u8(BUTTON_COUNTER_ADDR)
    frameCounter = mainmemory.read_u32_be(FRAME_COUNTER_ADDR)
    player1Position = mainmemory.read_u16_be(PLAYER_1_POSITION_ADDR) + 1
    lastSelectedItem = mainmemory.read_u8(LAST_SELECTED_ITEM_ADDR)
    calculatedRng = randomU16(previousRng)
    limitedRng = (calculatedRng / 65535.0 * 100.0) // 1
    itemRng = (limitedRng + buttonCounter + frameCounter + lastSelectedItem) % 100
    itemValue = ITEM_PROBABILITIES[player1Position][itemRng + 1]
    item = ITEMS[itemValue]

    printOutput()
end, 0x8007B410)


while true do
    lastSelectedItem = mainmemory.read_u8(LAST_SELECTED_ITEM_ADDR)
    printOutput()

    emu.frameadvance()
end